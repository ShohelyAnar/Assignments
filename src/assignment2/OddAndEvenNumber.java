package assignment2;

public class OddAndEvenNumber {

	public static void main(String[] args) {
		//1 to 20 find odd number ;
		for( int i=1;i<=20;i++) {
			if (i%2==0)
				System.out.println( i + " is even number,");
			else
				System.out.print( i + " is odd number,");
		}
		//100 to 125 find the even number;
		for( int j=100;j<=125;j++) {
			if(j%2==0)
				System.out.print( j + " is even number,");
			else
				System.out.println( j + " is odd number,");
			
			
		}}}

	


