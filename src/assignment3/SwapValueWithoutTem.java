package assignment3;

public class SwapValueWithoutTem {

	public static void main(String[] args) {
		int x=90;
		int y=60;
		System.out.println("Before swaping x: " +x);
		System.out.println("Before swaping y: " +y);
		x=x+y;
		y=x-y;
		x=x-y;
		System.out.println("After swaping x: " +x);
		System.out.println("After swaping y: " +y);

		int m=10;
		int n=15;
		System.out.println("Before swaping m: " +m);
		System.out.println("Before swaping n: " +n);
		m=m+n;
		n=m-n;
		m=m-n;
		System.out.println("After swaping m: " +m);
		System.out.println("After swaping n: " +n);
	
	
	}

}
